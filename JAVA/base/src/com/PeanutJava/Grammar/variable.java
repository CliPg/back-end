package com.PeanutJava.Grammar;

public class variable {
    public static void main(String[] args) {
       int a=0;
       int b=1;
        System.out.println(a);
        System.out.println(a+b);

        //修改变量的值,但变量不能重复定义
        a=3;
        System.out.println(a);

        //一条语句可以定义多个变量

    }
}
