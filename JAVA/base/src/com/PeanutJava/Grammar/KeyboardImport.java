package com.PeanutJava.Grammar;
//导包
import java.util.Scanner;

public class KeyboardImport {
    public static void main(String[] args) {
        //创建对象
        Scanner sc = new Scanner(System.in);
        //接收数据
        int i =sc.nextInt();
        //再接收数据
        int j=sc.nextInt();

        System.out.println(i);
        System.out.println(j);
    }
}
